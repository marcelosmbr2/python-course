# Integer

idade = 25 
temperatura = -10 
populacao = 215000000 

print("Idade:", idade, "Tipo: ", type(idade))
print("Temperatura:", temperatura, "Tipo: ", type(temperatura))
print("População:", populacao, "Tipo: ", type(populacao))

# Float

preco = 19.99
altura = 1.75
peso = 70.5

print("Preço:", preco, "Tipo: ", type(preco))
print("Altura:", altura, "Tipo: ", type(altura))
print("Peso:", peso, "Tipo: ", type(peso))

# String

nome = "Maria" 
cidade = 'Caxias do Sul' 
mensagem = "Olá, mundo!"

print("Nome:", nome, "Tipo: ", type(nome))
print("Cidade:", cidade, "Tipo: ", type(cidade))
print("Mensagem:", mensagem, "Tipo: ", type(mensagem))

# Boolean

is_student = True
is_employed = False

print("Is Student:", is_student, "Tipo: ", type(is_student))
print("Is Employed:", is_employed, "Tipo: ", type(is_employed))

# None

resultado = None
print("Resultado:", resultado, "Tipo: ", type(resultado))