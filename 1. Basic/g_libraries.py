# Importing Libraries 

import math

math.sqrt(16)       # 4.0 — square root
math.pi             # 3.14159...
math.ceil(4.2)      # 5 — rounds up
math.floor(4.8)     # 4 — rounds down
math.factorial(5)   # 120
math.log(100, 10)   # 2.0 — logarithm base 10 of 100

import os

os.getcwd()        # Returns the current working directory
os.listdir()       # Lists the files and directories in the current directory

import statistics

data = [1, 2, 3, 4, 5]
statistics.mean(data)       # 3.0 — mean
statistics.median(data)     # 3.0 — median
statistics.stdev(data)      # 1.58... — standard deviation


