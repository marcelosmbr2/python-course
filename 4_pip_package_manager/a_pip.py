# PIP 

# Pip is a package manager for Python
# It allows you to install and manage additional libraries and dependencies that are not included in the standard library

# To install a package using pip, you can use the following command in your terminal
# pip install package_name

# To uninstall a package, you can use
# pip uninstall package_name

# To list all installed packages, you can use
# pip list

# To show information about a specific package, you can use
# pip show package_name

# To upgrade a package to the latest version, you can use
# pip install --upgrade package_name

# To install a specific version of a package, you can use
# pip install package_name==version_number

# To publish the file requirements.txt, you can use
# pip freeze > requirements.txt

